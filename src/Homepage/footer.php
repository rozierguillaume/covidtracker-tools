        <h2>Qu'est-ce que la Covid19 ?</h2>
        <div shadow style="text-align: left">
            <!-- /wp:heading -->

            <!-- wp:paragraph -->
            <p>La maladie à Coronavirus 2019, appelée Covid-19, est provoquée par le coronavirus SARS-CoV-2. Elle est apparue en novembre 2019 dans le province de Hubei (Chine), dans la ville de Wuhan, et se propage depuis dans le monde entier.</p>
            <!-- /wp:paragraph -->

            <!-- wp:paragraph -->
            <p>Selon l’Organisation mondiale de la santé (OMS), le foyer de l’épidémie s’est déplacé. D’abord en Asie, il a concerné l’Europe en mars et avril 2020, puis l’Amérique en mai.</p>
